MinResult PackLine(
    uint8_t *pDstLine,
    const uint8_t *pSrcLine,
    uint8_t level,
    size_t count,
    bool invert) {
  if (pDstLine == NULL || pSrcLine == NULL)
    return MR_CONTRACT_VIOLATION;

  if (invert)
  {
    for (size_t x = 0; x < count; x++)
    {
      if (pSrcLine[x] < level)
        SETBIT(pDstLine, x);
      else
        CLRBIT(pDstLine, x);
    }
  }
  else
  {
    for (size_t x = 0; x < count; x++)
    {
      if (pSrcLine[x] < level)
        CLRBIT(pDstLine, x);
      else
        SETBIT(pDstLine, x);
    }
  }
  return MR_SUCCESS;
}

MinResult UnpackLine(
    uint8_t *pDstLine,
    const uint8_t *pSrcLine,
    size_t count,
    bool invert) {
  if (pDstLine == NULL || pSrcLine == NULL)
    return MR_CONTRACT_VIOLATION;

  size_t nBytes = count >> 3;
  size_t tail = count & 7;

  const uint64_t *pLUT = invert ? UNPACK_LUT_INV : UNPACK_LUT;

  for (size_t x = 0; x < nBytes; x++)
    ::memcpy(pDstLine + x * 8, &pLUT[pSrcLine[x]], 8);

  if (tail > 0)
    ::memcpy(pDstLine + nBytes * 8, &pLUT[pSrcLine[nBytes]], tail);

  return MR_SUCCESS;
}

MinResult CopyBits(
    uint8_t *pDstLine,
    const uint8_t *pSrcLine,
    size_t count,
    bool invert) {
  if (pDstLine == NULL || pSrcLine == 0)
    return MR_CONTRACT_VIOLATION;

  size_t nBytes = count >> 3;
//  size_t tail = count & 7;

  if (invert)
  {
    for (size_t x = 0; x < nBytes; x++)
      pDstLine[x] = ~pSrcLine[x];

    for (size_t x = (nBytes << 3); x < count; x++)
    {
      if (GETBIT(pSrcLine, x))
        CLRBIT(pDstLine, x);
      else
        SETBIT(pDstLine, x);
    }
  }
  else
  {
    for (size_t x = 0; x < nBytes; x++)
      pDstLine[x] = pSrcLine[x];

    for (size_t x = (nBytes << 3); x < count; x++)
    {
      if (GETBIT(pSrcLine, x))
        SETBIT(pDstLine, x);
      else
        CLRBIT(pDstLine, x);
    }
  }

  return MR_SUCCESS;
}
