/**
 * @mainpage Overview
 * <b>MinUtils</b> library is a part of MIN* library set that is obsolete and
 * should be disbanded. Most of functionality goes to <b>MinBase</b> library.
 *
 * The <b>MinUtils</b> library is header based, consisting of definitions, 
 * structures, classes with inline members, inline functions and templates, 
 * and as such do not need to be built in advance of its use.
 *
 * The library is written in C++ and can be compiled under Linux (GCC) and
 * Windows (MSVC 8 and later).
 */

